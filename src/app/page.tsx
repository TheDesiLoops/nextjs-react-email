"use client";
import Image from "next/image";
import { useState } from "react";

export default function Home() {
  const [sendingStatus, setSendingStatus] = useState<
    "idle" | "sending" | "sent" | "failed"
  >("idle");

  const [email, setEmail] = useState("");

  const onEmailSendingClick = async () => {
    if (sendingStatus !== "sending") {
      setSendingStatus("sending");
      try {
        const response = await fetch("/api/send-email", {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
          },
          body: JSON.stringify({ email }),
        });
        if (response.ok) {
          setSendingStatus("sent");
        }
      } catch (error) {
        console.error("Error sending email", error);
        alert("Error sending email");
        setSendingStatus("failed");
      }
    }
  };
  return (
    <div className="grid grid-rows-[20px_1fr_20px] items-center justify-items-center min-h-screen p-8 pb-20 gap-16 sm:p-20 font-[family-name:var(--font-geist-sans)]">
      <main className="flex flex-col gap-8 row-start-2 items-center">
        <Image
          className="dark:invert"
          src="/next.svg"
          alt="Next.js logo"
          width={180}
          height={38}
          priority
        />
        <div className="flex gap-4 items-center flex-col">
          <input
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            placeholder="Email address"
            className="rounded-lg border border-solid border-transparent transition-colors bg-foreground text-background px-4 h-10 sm:h-12"
          />
          <span
            className="rounded-full border border-solid border-transparent transition-colors flex items-center justify-center bg-foreground text-background gap-2 hover:bg-[#383838] dark:hover:bg-[#ccc] text-sm sm:text-base h-10 sm:h-12 px-4 sm:px-5 cursor-pointer"
            onClick={() => onEmailSendingClick()}
          >
            {sendingStatus === "sending" ? <Loader /> : <SendIcon />}
            Send Test Email
          </span>
          {sendingStatus === "sent" ? (
            <span className="text-green-500">Email sent successfully!</span>
          ) : sendingStatus === "failed" ? (
            <span className="text-red-500">Failed to send email</span>
          ) : null}
        </div>
      </main>
    </div>
  );
}

